<?php
$num1 = 22;
$num2 = 11;
$operator = "*";
if ($operator === "+") {
    $sum = $num1 + $num2;
    echo "Addition of $num1 and $num2 is: $sum";
    echo "<br />";
}
if ($operator === "-") {
    $substract = $num1 - $num2;
    echo "Subtraction of $num1 and $num2 is: $substract";
    echo "<br />";
}
if ($operator === "*") {
    $Multiply = $num1 * $num2;
    echo "Multiplication of $num1 and $num2 is: $Multiply";
    echo "<br />";
}
if ($operator === "/") {
    $Divide = $num1 / $num2;
    echo "Division of $num1 and $num2  is: $Divide";
}
?>